package strings;

public class Concatenation {
	public static void main(String[] args) {
		
		String firstname;
		String secondname;
		
		firstname= "rama";
		secondname="krishna";
		
		//using + sign	
		System.out.println(firstname + secondname);
		
		//using concatenation 	
		System.out.println(firstname.concat(secondname));
	}

}
