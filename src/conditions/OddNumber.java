package conditions;

public class OddNumber {
	public static void main(String[] args) {
		
		  int number = 7;

	        if (number % 2 != 0) {
	            System.out.println(number + " is an odd number.");
	        } else {
	            System.out.println(number + " is not an odd number.");
	        }
	}

}
